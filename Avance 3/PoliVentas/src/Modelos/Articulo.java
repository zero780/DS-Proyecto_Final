package Modelos;

import Observer.Vendedor;

/**
 *
 * @author usuario
 */
public class Articulo {
    
    protected Vendedor vendedor ;
    protected String nombre ;
    protected String categoria;
    protected float precio ;
    protected String tiempoEntrega;
    protected int vecesBuscados;
    protected String descripcion;
    protected String fechaPublicacion;

    public Articulo() {
    }

    
    
    public Vendedor getVendedor() {
        return vendedor;
    }

    public void setVendedor(Vendedor vendedor) {
        this.vendedor = vendedor;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCategoria() {
        return categoria;
    }

    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

    public float getPrecio() {
        return precio;
    }

    public void setPrecio(float precio) {
        this.precio = precio;
    }

    public String getTiempoEntrega() {
        return tiempoEntrega;
    }

    public void setTiempoEntrega(String tiempoEntrega) {
        this.tiempoEntrega = tiempoEntrega;
    }

    public int getVecesBuscados() {
        return vecesBuscados;
    }

    public void setVecesBuscados(int vecesBuscados) {
        this.vecesBuscados = vecesBuscados;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getFechaPublicacion() {
        return fechaPublicacion;
    }

    public void setFechaPublicacion(String fechaPublicacion) {
        this.fechaPublicacion = fechaPublicacion;
    }
    
    
    
}
